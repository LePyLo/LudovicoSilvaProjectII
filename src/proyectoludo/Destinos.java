public Object [][] getDestinosDatos(){
        
        int registros = 0;
        
        try{
            pst = con.conn.prepareStatement("SELECT count(1) as total FROM destinos");
            ResultSet res = pst.executeQuery();
            res.next();
            registros = res.getInt("total");
            res.close();
            
        }catch(SQLException ex){
            System.out.println(ex);
        }
        Object[][] data = new Object[registros][2];
        try{

            pst = con.conn.prepareStatement("SELECT"+" ID_destino, nombreDestino"+" FROM destinos ORDER BY ID_destino");
            ResultSet res = pst.executeQuery();
            int i = 0;
            while(res.next()){
                int idDestino = Integer.parseInt(res.getString("ID_destino"));
                String nombreDestino = res.getString("nombreDestino");

                data[i][0] = idDestino;
                data[i][1] = nombreDestino;
                i++;
            }
            res.close();
        }catch(SQLException ex){
            System.out.println(ex);
        }
        return data;
    }
