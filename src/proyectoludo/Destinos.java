    private void destinos_btn_delMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_destinos_btn_delMouseClicked
        if(User.Rank <2){
            JOptionPane.showMessageDialog(null, "No tienes permiso para realizar esta accion","Informacion",JOptionPane.OK_OPTION);
        }
        else{
            if(destinos_jtf_nombre.getText().isEmpty() || destinos_jtf_nombre.getText()==""){
            JOptionPane.showMessageDialog(this, "Porfavor escriba o seleccione el nombre del destino a eliminar","Eliminar destino",JOptionPane.OK_OPTION);
            }else{
                String empresaName = destinos_jtf_nombre.getText(); 
                try{
                    pst = con.conn.prepareStatement("SELECT * FROM `destinos` WHERE `nombreDestino`=?");
                    pst.setString(1, empresaName);
                    ResultSet rs = pst.executeQuery();
                    rs.next();
                    int pos = rs.getInt("ID_destino");
                    rs.close();
                    if(pos <=0){
                        JOptionPane.showMessageDialog(this, "Este destino no existe","Eliminar destino",JOptionPane.OK_OPTION);
                    }else{
                        pst = con.conn.prepareStatement("DELETE FROM `destinos` WHERE `nombreDestino`=?");
                        pst.setString(1, empresaName);
                        pst.executeUpdate();
                        JOptionPane.showMessageDialog(this, "Destino eliminado correctamente","Eliminar destino",JOptionPane.OK_OPTION);
                    }
                    
                }catch(SQLException ex){
                    System.out.println("ERROR ELIMINAR DESTINO: "+ex.getMessage());
                    JOptionPane.showMessageDialog(this, "Ocurrio un error al tratar de eliminar el destino","Eliminar destino",JOptionPane.ERROR_MESSAGE);
                }finally{
                    update_destinoTable();
                    limpiarCampos();
                }
                
            }
            
        }
        
        
    }//GEN-LAST:event_destinos_btn_delMouseClicked

