        DefaultTableModel tm_datos = new DefaultTableModel(datosDestinos,columnNames){
            @Override
            public boolean isCellEditable(int row, int column) {
            return false;
        }
        };
        destinos_tbl_desti.setModel(tm_datos);
        
        final TableColumnModel columnModel = destinos_tbl_desti.getColumnModel();
        for (int column = 0; column < destinos_tbl_desti.getColumnCount(); column++) {
            int width = 15; // Min width
            for (int row = 0; row < destinos_tbl_desti.getRowCount(); row++) {
                TableCellRenderer renderer = destinos_tbl_desti.getCellRenderer(row, column);
                Component comp = destinos_tbl_desti.prepareRenderer(renderer, row, column);
                width = Math.max(comp.getPreferredSize().width +1 , width);
            }
            if(width > 300)
                width=300;
            columnModel.getColumn(column).setPreferredWidth(width);
        }
        
