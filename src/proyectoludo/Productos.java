    public void update_productosTable(){
        clearTable();
        Object[][] dtPer;
        String[] columnNames ={"ID producto","Nombre del producto", "Tipo producto"};
        dtPer = getProductosDatos();
        DefaultTableModel datos = new DefaultTableModel(dtPer,columnNames){
            @Override
            public boolean isCellEditable(int row, int column) {
            return false;
        }
        };
        productos_tbl_desti.setModel(datos);
        final TableColumnModel columnModel = productos_tbl_desti.getColumnModel();
        for (int column = 0; column < productos_tbl_desti.getColumnCount(); column++) {
            int width = 15; // Min width
            for (int row = 0; row < productos_tbl_desti.getRowCount(); row++) {
                TableCellRenderer renderer = productos_tbl_desti.getCellRenderer(row, column);
                Component comp = productos_tbl_desti.prepareRenderer(renderer, row, column);
                width = Math.max(comp.getPreferredSize().width +1 , width);
            }
            if(width > 300)
                width=300;
            columnModel.getColumn(column).setPreferredWidth(width);
        }

    }

public Object [][] getProductosDatos(){
        
        int registros = 0;
        
        try{
            pst = con.conn.prepareStatement("SELECT count(1) as total FROM productos");
            ResultSet res = pst.executeQuery();
            res.next();
            registros = res.getInt("total");
            res.close();
            
        }catch(SQLException ex){
            System.out.println(ex);
        }
        Object[][] data = new String[registros][3];
        try{

            pst = con.conn.prepareStatement("SELECT"+" ID_producto, nombreProd, tipoProducto"+" FROM productos ORDER BY ID_producto");
            ResultSet res = pst.executeQuery();
            int i = 0;
            while(res.next()){
                String idProducto = res.getString("ID_producto");
                String nombreProducto = res.getString("nombreProd");
                String tipoProducto = res.getString("tipoProducto");

                data[i][0] = idProducto;
                data[i][1] = nombreProducto;
                data[i][2] = tipoProducto;
                i++;
            }
            res.close();
        }catch(SQLException ex){
           
            System.out.println("ERROR getProductosDatos(): "+ex);
        }
        return data;
    }
