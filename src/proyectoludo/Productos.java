    private void productos_btn_delMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_productos_btn_delMouseClicked
        if(User.Rank <2){
            JOptionPane.showMessageDialog(null, "No tienes permiso para realizar esta accion","Informacion",JOptionPane.OK_OPTION);
        }
        else{
            if(productos_jtf_id.getText().isEmpty() || productos_jtf_id.getText()=="0"){
            JOptionPane.showMessageDialog(this, "Porfavor escriba o seleccione el nombre del producto a eliminar","Eliminar producto",JOptionPane.OK_OPTION);
            }else{
                //String empresaName = productos_jtf_nombre.getText(); 
                try{
                    pst = con.conn.prepareStatement("SELECT * FROM `productos` WHERE `ID_producto`=?");
                    pst.setString(1, productos_jtf_id.getText());
                    ResultSet rs = pst.executeQuery();
                    rs.next();
                    int pos = rs.getInt("ID_producto");
                    rs.close();
                    if(pos <=0){
                        JOptionPane.showMessageDialog(this, "Este producto no existe","Eliminar producto",JOptionPane.OK_OPTION);
                    }else{
                        pst = con.conn.prepareStatement("DELETE FROM `productos` WHERE `ID_producto`=?");
                        pst.setString(1, productos_jtf_id.getText());
                        pst.executeUpdate();
                        JOptionPane.showMessageDialog(this, "Producto eliminado correctamente","Eliminar producto",JOptionPane.OK_OPTION);
                    }
                    
                }catch(SQLException ex){
                    System.out.println("ERROR ELIMINAR PRODUCTO: "+ex.getMessage());
                    JOptionPane.showMessageDialog(this, "Ocurrio un error al tratar de eliminar el producto","Eliminar producto",JOptionPane.ERROR_MESSAGE);
                }finally{
                    update_productosTable();
                    limpiarCampos();
                }
                
            }
            
        }
        
        
    }//GEN-LAST:event_productos_btn_delMouseClicked

