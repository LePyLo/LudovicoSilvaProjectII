    public void update_empresaTable(){
        clearTable();
        Object[][] dtPer;
        String[] columnNames ={"ID empresa","Nombre de la empresa"};
        dtPer = getEmpresasDatos();
        DefaultTableModel datos = new DefaultTableModel(dtPer,columnNames){
            @Override
            public boolean isCellEditable(int row, int column) {
            return false;
        }
        };
        empresas_tbl_empre.setModel(datos);
        final TableColumnModel columnModel = empresas_tbl_empre.getColumnModel();
        for (int column = 0; column < empresas_tbl_empre.getColumnCount(); column++) {
            int width = 15; // Min width
            for (int row = 0; row < empresas_tbl_empre.getRowCount(); row++) {
                TableCellRenderer renderer = empresas_tbl_empre.getCellRenderer(row, column);
                Component comp = empresas_tbl_empre.prepareRenderer(renderer, row, column);
                width = Math.max(comp.getPreferredSize().width +1 , width);
            }
            if(width > 300)
                width=300;
            columnModel.getColumn(column).setPreferredWidth(width);
        }

    }

public Object [][] getEmpresasDatos(){
        
        int registros = 0;
        
        try{
            pst = con.conn.prepareStatement("SELECT count(1) as total FROM empresas");
            ResultSet res = pst.executeQuery();
            res.next();
            registros = res.getInt("total");
            res.close();
            
        }catch(SQLException ex){
            System.out.println(ex);
        }
        Object[][] data = new String[registros][2];
        try{

            pst = con.conn.prepareStatement("SELECT"+" ID_empresa, nombreEmpresa"+" FROM empresas ORDER BY ID_empresa");
            ResultSet res = pst.executeQuery();
            int i = 0;
            while(res.next()){
                String idEmpresa = res.getString("ID_empresa");
                String nombreEmpresa = res.getString("nombreEmpresa");

                data[i][0] = idEmpresa;
                data[i][1] = nombreEmpresa;
                i++;
            }
            res.close();
        }catch(SQLException ex){
            System.out.println(ex);
        }
        return data;
    }
