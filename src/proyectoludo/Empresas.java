    private void empresas_btn_delMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_empresas_btn_delMouseClicked
        if(User.Rank <2){
            JOptionPane.showMessageDialog(null, "No tienes permiso para realizar esta accion","Informacion",JOptionPane.OK_OPTION);
        }
        else{
            if(empresas_jtf_nombre.getText().isEmpty() || empresas_jtf_nombre.getText()==""){
            JOptionPane.showMessageDialog(this, "Porfavor escriba o seleccione el nombre de la empresa a eliminar","Eliminar empresa",JOptionPane.OK_OPTION);
            }else{
                String empresaName = empresas_jtf_nombre.getText(); 
                try{
                    pst = con.conn.prepareStatement("SELECT * FROM `empresas` WHERE `nombreEmpresa`=?");
                    pst.setString(1, empresaName);
                    ResultSet rs = pst.executeQuery();
                    rs.next();
                    int pos = rs.getInt("ID_empresa");
                    rs.close();
                    if(pos <=0){
                        JOptionPane.showMessageDialog(this, "Esta empresa no existe","Eliminar empresa",JOptionPane.OK_OPTION);
                    }else{
                        pst = con.conn.prepareStatement("DELETE FROM `empresas` WHERE `nombreEmpresa`=?");
                        pst.setString(1, empresaName);
                        pst.executeUpdate();
                        JOptionPane.showMessageDialog(this, "Empresa eliminada correctamente","Eliminar empresa",JOptionPane.OK_OPTION);
                    }
                    
                }catch(SQLException ex){
                    System.out.println("ERROR ELIMINAR EMPRESA: "+ex.getMessage());
                    JOptionPane.showMessageDialog(this, "Ocurrio un error al tratar de eliminar la empresa","Eliminar empresa",JOptionPane.ERROR_MESSAGE);
                }finally{
                    update_empresaTable();
                    limpiarCampos();
                }
                
            }
            
        }
        
        
    }//GEN-LAST:event_empresas_btn_delMouseClicked

